  <div class="faq-list">
    <div class="faq-item">
      <div class="faq-q">Welche Krypto-Plattform ist in Deutschland reguliert? <span class="faq-arrow">▾</span></div>
      <div class="faq-a">Trade Republic und Bison (Börse Stuttgart) sind direkt von der BaFin reguliert. Bitpanda hat eine EU-Lizenz aus Österreich. Kraken und Coinbase sind international reguliert, aber nicht direkt durch die BaFin.</div>
    </div>
    <div class="faq-item">
      <div class="faq-q">Wie werden Krypto-Gewinne in Deutschland versteuert? <span class="faq-arrow">▾</span></div>
      <div class="faq-a">Kryptowährungen gelten als private Veräußerungsgeschäfte. Gewinne sind steuerfrei bei einer Haltedauer von über einem Jahr. Bei kürzerer Haltedauer wird mit dem persönlichen Einkommenssteuersatz versteuert. Die Freigrenze liegt bei 1.000 € pro Jahr.</div>
    </div>
    <div class="faq-item">
      <div class="faq-q">Was ist der Unterschied zwischen einer Krypto-Börse und einem Broker wie Trade Republic? <span class="faq-arrow">▾</span></div>
      <div class="faq-a">Traditionelle Krypto-Börsen wie Kraken oder Bitpanda spezialisieren sich auf den Handel vieler Coins. Broker wie Trade Republic ermöglichen Krypto als Asset neben ETFs und Aktien – einfacher, aber mit weniger Tiefe und Coin-Auswahl.</div>
    </div>
    <div class="faq-item">
      <div class="faq-q">Sollte ich Krypto auf der Börse lassen oder in eine eigene Wallet übertragen? <span class="faq-arrow">▾</span></div>
      <div class="faq-a">Für kleinere Beträge ist die Verwahrung auf einer regulierten Plattform praktisch. Für größere Summen gilt: "not your keys, not your coins" – eine Hardware-Wallet wie Ledger gibt volle Kontrolle über das eigene Krypto.</div>
    </div>
    <div class="faq-item">
      <div class="faq-q">Ist ein Krypto-Sparplan sinnvoll? <span class="faq-arrow">▾</span></div>
      <div class="faq-a">Ja – ein regelmäßiger Sparplan (Cost Averaging) reduziert das Risiko, zu einem ungünstigen Zeitpunkt einzukaufen. Trade Republic und Bitpanda bieten Krypto-Sparpläne an. Für volatile Assets wie Bitcoin ist diese Strategie besonders empfehlenswert.</div>
    </div>
